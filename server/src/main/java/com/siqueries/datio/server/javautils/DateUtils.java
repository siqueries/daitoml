    /**
     * Determine SimpleDateFormat pattern matching with the given date string.
     * Returns null if format is unknown. You can simply extend DateUtil with
     * more formats if needed.
     *
     * @param dateString The date string to determine the SimpleDateFormat
     *                   pattern for.
     * @return The matching SimpleDateFormat pattern, or null if format is
     * unknown.
     * @see SimpleDateFormat
     */
    public static String determineDateTimeFormat(String dateString) {
        for (String regexp : DATE_FORMAT_REGEXPS.keySet()) {
            if (dateString.toLowerCase().matches(regexp)) {
                return DATE_FORMAT_REGEXPS.get(regexp);
            }
        }
        return null; // Unknown format.
    }

    public static String determineDateFormat(String dateString) {
        for (String regexp : DATE_REGEXPS.keySet()) {
            if (dateString.toLowerCase().matches(regexp)) {
                return DATE_REGEXPS.get(regexp);
            }
        }
        return null; // Unknown format.
    }

    public static String parseDate(String dateString, String format) {
        DateFormat srcDf = new SimpleDateFormat(format);

        // parse the date string into Date object
        Date date = null;
        try {
            date = srcDf.parse(dateString);
        } catch (ParseException e) {
            // TODO Auto-generated catch block
            e.printStackTrace();
        }

        DateFormat destDf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");

        // format the date into another format
        String dateStr = destDf.format(date);

        return dateStr;
    }

